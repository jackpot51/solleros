setpitch:
	mov al, 0xB6
	out 0x43, al
	mov ax, [esi]
	out 0x42, al
	mov al, ah
	out 0x42, al
startsound:
	in al, 0x61
	or al, 3
	out 0x61, al
	
keytest:
	mov al, 0
	mov ah, 5
	int 30h	;wait for keypress
	jmp killsound
	
killsound:
	in al, 0x61
	and al, 0xFC
	out 0x61, al
	jmp exit
	
